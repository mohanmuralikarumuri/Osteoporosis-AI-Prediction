// ─── Context ──────────────────────────────────────────────────────────────
const LanguageContext = createContext(null)

export function LanguageProvider({ children }) {
  const [lang, setLang] = useState('en')
  const t = TRANSLATIONS[lang] ?? TRANSLATIONS.en
  const currentLang = LANGUAGES.find((l) => l.code === lang)

  // Apply RTL direction to the document when needed
  const changeLang = (code) => {
    setLang(code)
    const dir = LANGUAGES.find((l) => l.code === code)?.dir ?? 'ltr'
    document.documentElement.dir  = dir
    document.documentElement.lang = code
  }

  return (
    <LanguageContext.Provider value={{ lang, setLang: changeLang, t, currentLang }}>
      {children}
    </LanguageContext.Provider>
  )
}

/** Hook to consume translations in any component */
export function useLang() {
  return useContext(LanguageContext)
}
